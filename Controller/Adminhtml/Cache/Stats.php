declare(strict_types=1);

namespace Qoliber\TridentCache\Controller\Adminhtml\Cache;

use Magento\Backend\App\Action;
use Magento\Backend\App\Action\Context;
use Magento\Framework\View\Result\Page;
use Magento\Framework\View\Result\PageFactory;

class Stats extends Action
{
    public const ADMIN_RESOURCE = 'Qoliber_TridentCache::stats';

    public function __construct(
        Context $context,
        private readonly PageFactory $resultPageFactory
    ) {
        parent::__construct($context);
    }

    public function execute(): Page
    {
        $resultPage = $this->resultPageFactory->create();
        $resultPage->setActiveMenu('Qoliber_TridentCache::stats');
        $resultPage->getConfig()->getTitle()->prepend(__('Trident Cache Statistics'));

        return $resultPage;
    }
}
